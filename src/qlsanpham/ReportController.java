    private void checkButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_checkButtonActionPerformed
        int day;
        if (this.dayTextField.getText().isEmpty()) {
            day = 0;
        } else {
            day = Integer.valueOf(this.dayTextField.getText());
        }

        int month;
        if (this.monthTextField.getText().isEmpty()) {
            month = 0;
        } else {
            month = Integer.valueOf(this.monthTextField.getText());
        }

        int year;
        if (this.yearTextField.getText().isEmpty()) {
            year = 0;
        } else {
            year = Integer.valueOf(this.yearTextField.getText());
        }

        this.getReport(day, month, year);
    }//GEN-LAST:event_checkButtonActionPerformed

