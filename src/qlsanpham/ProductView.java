    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        productId = Integer.valueOf(this.productsTable.getValueAt(this.productsTable.getSelectedRow(), 0).toString());
        Product product = this.controller.entity.get(productId);
        this.nameProducts.setText(product.name);
        this.priceProducts.setText(String.valueOf(product.price));
        this.descriptionProducts.setText(product.description);
        this.imageProducts.setText(product.image);
        this.categoriesSelect.setSelectedItem(product.getCategories().getName());
    }//GEN-LAST:event_jButton2ActionPerformed

