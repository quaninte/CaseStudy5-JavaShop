    void addProductsToTable(int id, String name, String image, int price, String description, String categoriesName) {
        Vector row = new Vector();
         row.addElement(id);
         row.addElement(name);
         row.addElement(image);
         row.addElement(price);
         row.addElement(description);
         row.addElement(categoriesName);
         this.tableModel.addRow(row);

    }
    
    void clearTable() {
           while(this.tableModel.getRowCount() > 0) {
           this.tableModel.removeRow(0);
    }
    }

